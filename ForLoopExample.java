public class ForLoopExample {
    public static void main(String[] args){
        System.out.println("Increment i.e ++");
        for(int i=0;i<=10;i++) {
            System.out.println(i);
            }
        System.out.println("Example of For Loop Decrement ");

        for(int j=100;j>0;) {
             j-=10;
            System.out.println(j);
        }

        }
    }

